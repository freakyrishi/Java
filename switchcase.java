
public class switchcase {
	public static void main(String[] args) {
		int ch=3;
		
		switch (ch)
		{
		case 1:
			System.out.println("This is number 1");
			break;
		case 2:
			System.out.println("This is number 2");
			break;
		case 3:
			System.out.println("This is number 3");
			break;
		default :
			System.out.println("Wrong Number");
			break;
		
		}
	}
} 
